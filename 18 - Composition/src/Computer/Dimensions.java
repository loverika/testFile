package Computer;

public class Dimensions {
	private int width;
	private int heiht;
	private int depth;
	
	
	public Dimensions(int width, int heiht, int depth) {
		this.width = width;
		this.heiht = heiht;
		this.depth = depth;
	}

	public int getWidth() {
		return width;
	}


	public int getHeiht() {
		return heiht;
	}


	public int getDepth() {
		return depth;
	}

}
